#Ingresa los dos números a utilizar
x = int (input("Ingresa el primer número "))
y = int (input("Ingresa el segundo número "))

#Suma
z = x + y
print("Suma: ")
print( x, " + ", y," = ", z)

#Resta
z = x - y
print("Resta: ")
print( x, " - ", y," = ", z)

#Multiplicación
z = x * y
print("Multiplicación: ")
print( x, " * ", y," = ", z)

#División
z = x / y
print("División: ")
print( x, " / ", y," = ", z)

#División sin número decimal
z = x // y
print("División sin número decimal: ")
print( x, " // ", y," = ", z)

#Potencia
z = x ** y
print("Potencia: ")
print( x, " ^ ", y," = ", z)
