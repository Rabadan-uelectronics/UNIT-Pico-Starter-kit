print("Ciclo for de uno en uno")
for i in range(0,10):
    #No olvides insertar un espacio o sangría en las líneas que se ejecutarán dentro del for
    print("Número de ciclo: ", i)

print("Ciclo for de dos en dos")
for i in range(0,10,2): #Este ciclo contará de dos en dos cada ejecución
    print("Número de ciclo: ", i)
    
print("Ciclo for descendente")
for i in range(10,0,-1): #Este ciclo se ejecutará de forma descendente
    print("Número de ciclo: ", i)
